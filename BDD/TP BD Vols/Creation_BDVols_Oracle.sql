CREATE TABLE PILOTE(
  PLNUM NUMBER(4) PRIMARY KEY,
  PLNOM VARCHAR2(15) NOT NULL,
  PLPRENOM VARCHAR2(15) NOT NULL,
  VILLE VARCHAR2(15) NULL,
  SALAIRE NUMBER(7,2) NULL);

CREATE TABLE AVION(
  AVNUM NUMBER(4) PRIMARY KEY,
  AVNOM VARCHAR2(8) NOT NULL,
  CAPACITE NUMBER(4) NULL,
  LOCALISATION VARCHAR2(9) NULL);

CREATE TABLE VOL(
  VOLNUM NUMBER(3) PRIMARY KEY,
  PLNUM NUMBER(4) NOT NULL,
  AVNUM NUMBER(4) NOT NULL,
  VILLEDEP VARCHAR2(15) NOT NULL,
  VILLEARR VARCHAR2(15) NOT NULL,
  HEUREDEP DATE NOT NULL,
  HEUREARR DATE NOT NULL,
  FOREIGN KEY (PLNUM) REFERENCES PILOTE(PLNUM),
  FOREIGN KEY (AVNUM) REFERENCES AVION(AVNUM));

